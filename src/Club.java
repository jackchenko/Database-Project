public class Club {

    private String club;
    private String desc;

    public Club(String club, String desc) {
        this.club = club;
        this.desc = desc;
    }

    public String getClub() {
        return club;
    }

    public void setClub(String club) {
        this.club = club;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
